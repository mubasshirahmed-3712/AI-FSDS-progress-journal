# Arithmetic operations
print(3 + 2)   # Addition
print(3 - 2)   # Subtraction
print(3 * 2)   # Multiplication
print(3 / 2)   # Division (returns float)
print(3 ** 2)  # Exponentiation (3^2)
print(3 % 2)   # Modulus (remainder)
print(3 // 2)  # Floor division (rounds down)

# Checking various data types
print(type(10))                    # int
print(type(3.14))                  # float
print(type(1 + 3j))                # complex number
print(type('Mubasshir'))           # string
print(type([1, 2, 3]))             # list
print(type({'name': 'Mubasshir'}))# dictionary
print(type({9.8, 3.14, 2.7}))      # set
print(type((9.8, 3.14, 2.7)))      # tuple
print(type(3 == 3))                # bool
print(type(3 >= 3))                # bool
